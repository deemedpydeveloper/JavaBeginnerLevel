public class EscapeSequences
{
 public static void main(String[] args)
    {
        // \n ---> new line.
        // \t --> horizontal tab space.
        // \b --> one character back space.
        // \" ---> double quote.
        // \' ---> singlequote.
        // \\ ---> backslash.
        System.out.println("My Name is Y.Tejendra.\nI live in Warangal<--\t-->Hanamkonda.\nIam a Graduates\b.");
        System.out.println("My favourite line is: \"Work Hard Until You Get Success\".\n");
        System.out.println("You can call me tej \\ tejendra.");
    }
   
}
