public class Comments
{
    /**
     * this is a documentation type comment.
     * it is used to describe the given code or function in a java file.
     **/
    public static void main(String[] args)
    {
        /* this is a multi-line comment.
        it is used to decribe the code.
        */
        System.out.println("Single Line Comments - Syntax:\n \\\\Write here."); // this is a single line comment.
        System.out.println("Multi-Line Comments - Syntax:\n \\*Write Here\nWrite Here*\\");
        System.out.println("Documentation Comments - Syntax:\n \\** *Write Here \n *Write Here **\\");
    }
}
