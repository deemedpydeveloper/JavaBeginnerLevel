public class BitWiseOperators
{
     public static void main(String[] args)
     {
        int a = 10, b = 20;
        
        System.out.println("AND: "+(a & b));
        
        System.out.println("OR: "+(a + b));

        System.out.println("EX-OR: "+(a ^ b));

        System.out.println("NEGATION: "+(~a));
        
    }
}
