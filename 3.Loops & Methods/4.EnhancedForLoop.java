public class EnhancedForLoop
{
    public static void main(String[] args)
    {
        String colors[] = {"red", "orange", "blue", "yellow", "pink"}; //  array initialization.
        // EnhancedForLoop Structure.
        for (String a:colors) // a is string data type which takes each string from colors array every iteration.
            System.out.println(a);
    }
}
